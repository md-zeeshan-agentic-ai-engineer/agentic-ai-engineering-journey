# ===== Age Checker =====

age = int(input("Enter your age: "))
if age >= 18:
    print("Adult")
else:
    print("Minor")




# ===== Number Checker =====

num = int(input("Enter a number: "))
if num > 0:
    print("Positive")
elif num < 0:
    print("Negative")
else:
    print("Zero")






